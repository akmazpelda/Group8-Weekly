package PeldaHanar.week2;

public class SwapTwoNumbers {
    public static void main(String[] args) {

        //Swap two variable values without using a third variable

        int a = 25;
        int b = 21;

        a = a + b;  //46
        b = a - b;  //4
        a = a - b;  //4

        System.out.println("a = " + a);
        System.out.println("b = " + b);


    }
}
